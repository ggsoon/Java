public class Practicestring {
    public static void main(String[] args) {
        //sout
        System.out.println(6); // number
        System.out.println("six"); // string
        System.out.println('s'); // character

        System.out.println(6+6);
        System.out.println("6"+"6");
        //System.out.println("6"*"6");

        System.out.println("1111".length());
        System.out.println("Hello User : Minge".replace("Minge", "Lee"));
        System.out.println("Hello User : [[[name]]]".replace("[[[name]]]", "Kim"));
        System.out.println("Hello User : kim".replace("kim", "[[[name]]]"));

        //System.out.println(1111.length);

        System.out.println(Math.PI);
        System.out.println(Math.floor(Math.PI)); // floor 내림 ceil 올림

        System.out.println("first \nsecond");
        System.out.println("Hi \"Hello\"");

    }
}